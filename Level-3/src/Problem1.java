public class Problem1 {
    public static void main(String[] args) {
        double length = 5.0;
        double breadth = 7.0;
        double area = length * breadth;
        double perimeter = 2 * (length + breadth);
        System.out.println("Length of the rectangle: " + length + " units");
        System.out.println("Breadth of the rectangle: " + breadth + " units");
        System.out.println("Area of the rectangle: " + area + " square units");
        System.out.println("Perimeter of the rectangle: " + perimeter + " units");
    }
}
